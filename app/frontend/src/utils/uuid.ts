/**
 * Generate a UUID v4 string
 * Uses crypto.randomUUID when available (secure contexts),
 * falls back to a polyfill implementation for non-secure contexts
 */
export function generateUUID(): string {
  // Try native crypto.randomUUID first (requires secure context)
  if (typeof crypto !== 'undefined' && typeof crypto.randomUUID === 'function') {
    return crypto.randomUUID();
  }

  // Fallback implementation for non-secure contexts
  // This uses Math.random() which is not cryptographically secure
  // but sufficient for client-side message/thread IDs
  return 'xxxxxxxx-xxxx-4xxx-yxxx-xxxxxxxxxxxx'.replace(/[xy]/g, (c) => {
    const r = (Math.random() * 16) | 0;
    const v = c === 'x' ? r : (r & 0x3) | 0x8;
    return v.toString(16);
  });
}
